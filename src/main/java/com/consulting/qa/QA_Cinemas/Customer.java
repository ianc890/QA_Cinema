package com.consulting.qa.QA_Cinemas;

public class Customer {
	
	private String type;
	private int age;
	
	public Customer() {
		
	}
	
	public String getType() {
		return type;
	}
	
	public void setType(String t) {
		this.type = t;
	}
	
	public int getAge() {
		return age;
	}
	
	public void setAge(int a) {
		this.age = a;
	}

}
